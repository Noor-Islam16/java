package com.codewithharry;

public class Operators {
    public static void main(String[] args) {
        int b = 9;//arithmetic operator
        b += 6;
        System.out.println(b);
        System.out.println(64>8); //comparison operator
        System.out.println(77>7 && 44>4); //logical and
        System.out.println(64<8);//comparison
        System.out.println(6==6);//equal to
        System.out.println(6>5 || 6>9);//logical or
    }
}
